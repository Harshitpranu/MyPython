from turtle import *
t=Turtle()
t.pencolor('blue')
t.penup()
t.goto(-200,-50)
t.pendown()
t.begin_fill()
t.fillcolor("yellow")
t.lt(60)
t.fd(400)
t.rt(120)
t.fd(400)
t.rt(120)
t.fd(400)
t.penup()
t.goto(200,180)
t.pendown()
t.fd(400)
t.lt(120)
t.fd(400)
t.lt(120)
t.fd(400)
t.end_fill()
t.hideturtle()
done()